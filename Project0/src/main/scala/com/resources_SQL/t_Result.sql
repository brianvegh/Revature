     FOREIGN KEY (`RaceNumber`) REFERENCES `Schedule`(RaceNumber),
     FOREIGN KEY (`DriverID`) REFERENCES `Driver`(ID),
     FOREIGN KEY (`Position`) References `Points`(Position)
